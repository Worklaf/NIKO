let currentLang = localStorage.getItem('language') || 'en';

function t(key) {
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

function switchLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('language', lang);
  
  document.querySelectorAll('[id^="lang-"]').forEach(btn => btn.classList.remove('active'));
  const langBtn = document.getElementById('lang-' + lang);
  if (langBtn) langBtn.classList.add('active');
  
  applyTranslations();
}

function applyTranslations() {
  // placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(node => {
    const key = node.dataset.i18nPlaceholder;
    if (key) node.placeholder = t(key);
  });

  // elements with text
  document.querySelectorAll('[data-i18n]').forEach(node => {
    const key = node.dataset.i18n;
    if (key) node.textContent = t(key);
  });

  // options (general)
  document.querySelectorAll('option[data-i18n]').forEach(opt => {
    opt.textContent = t(opt.dataset.i18n);
  });

  // special cases
  const siteSearch = document.getElementById('siteSearch');
  if (siteSearch) siteSearch.placeholder = t('searchPlaceholder');

  const emptyGenre = document.querySelector('#siteGenre option[value=""]');
  if (emptyGenre) emptyGenre.textContent = t('allGenres');

  // upload permission select with arrow prefix
  document.querySelectorAll('#upload-permission-select option[data-i18n]').forEach(opt => {
    opt.textContent = '⬆ ' + t(opt.dataset.i18n);
  });

  // play-count text node
  document.querySelectorAll('.play-count').forEach(node => {
    const textNode = Array.from(node.childNodes).find(n => n.nodeType === 3 && n.textContent.trim());
    if (textNode) textNode.textContent = ' ' + t('plays');
  });
}

